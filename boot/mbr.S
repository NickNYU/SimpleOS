;main mbr
;----------------------------------------------------------------------------------
SECTION MBR vstart=0x7c00
    mov ax, cs
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov fs, ax
    mov sp, 0x7c0

; 清屏利用 Ox06 号功能，上卷全部行，贝IJ 可清屏
; -----------------------------------------------------------
; INT OxlO 功能号： Ox06 功能描述：上卷窗口
;------------------------------------------------------------
;输入：
;AH 功能号＝ Ox06 
; AL ＝上卷的行数（如果为 ，表示全部）
; BH ＝上卷行属性
; (CL, CH) ＝窗口左上角的（ ）位置
; (DL, DH) ＝窗口右下角的（X, ）位置
; 无返回值：
    mov ax, 0x600
    mov bx, 0x700
    mov cx, 0               ; 左上角: (0, 0)
    mov dx, 0x184f          ; 右下角: (80, 25)

    int 0x10

;;;;;;;  下面这三行代码获取光标位置  ;;;;;;;;;;;;;;
    mov ah, 3
    mov bh, 0

    int 0x10

;;;;;;;  打印字符串  ;;;;;;;;;;;;;;;;;;;;;;;;;;;
    mov ax, message
    mov bp, ax

    mov cx, 5
    mov ax, 0x1301

    mov bx, 0x2

    int 0x10

;;;;;; 进入等待 ;;;;;;;;;;;;;;;;;;;;;;;
    jmp $

    message db "1 MBR"
    times 510-($-$$) db 0
    db 0x55, 0xaa